/**
 * @brief Inicialização das portas.
 * @param None.
 * @return None.
 */
void init_gpio()
{
    // LEDs
    pinMode(GPIO_STRIP_DOUT, OUTPUT);
    pinMode(GPIO_LEDS_DOUT, OUTPUT);

    // Sensores
    pinMode(GPIO_POWER_SENSOR, INPUT);
    pinMode(GPIO_LEFTOVER_SENSOR, INPUT);

    // Demais portas não necessitam de inicialização.
}
